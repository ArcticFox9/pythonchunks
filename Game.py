import os
import sys,time,random # for the slow type

typing_speed = 75 # wpm

# function for slow type
def slow_type(t):
    for l in t:
        sys.stdout.write(l)
        sys.stdout.flush()
        time.sleep(random.random()*10.0/typing_speed)
